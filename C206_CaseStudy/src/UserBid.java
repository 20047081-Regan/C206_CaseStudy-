
public class UserBid {
	private String name;
	private String password;
	
	public UserBid(String name, String password) {
		this.name = name;
		this.password = password;
	}
	
	public String getName() {
		String userName = name;
		return userName;
	}
	
	public String getPassword() {
		String passWord = password;
		return passWord;
	}

}
